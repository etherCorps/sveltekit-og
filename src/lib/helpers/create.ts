import type { Component } from "svelte";

import type { SatoriOptions } from "satori";
import type { ResvgRenderOptions } from "@resvg/resvg-wasm";

import { type EmojiType, loadDynamicAsset } from "./emoji.js";
import { default_fonts, DEFAULT_WIDTH } from "../helpers/defaults.js";
import { useResvg, useSatori } from "../providers/instances.js";
import type { ComponentOptions, ImageOptions } from "../types.js";
import { createVNode } from "./toJSX.js";
import { logger } from "./logger.js";
import { handleAsyncAll, handleAsync, ErrorCodes } from "./error-handler.js";

export async function createSvg(
	element: string | Component,
	imageOptions: ImageOptions,
	componentOptions?: ComponentOptions
): Promise<string> {
	const [satori, vnodes] = await handleAsyncAll(
		[() => useSatori(), () => Promise.resolve(createVNode(element, componentOptions))] as const,
		ErrorCodes.SATORI_RENDER_FAILED,
		"Failed to initialize Satori or create VNode"
	);

	const satoriOptions = structuredClone(imageOptions) as SatoriOptions;
	if (!Object.hasOwn(satoriOptions, "fonts")) {
		satoriOptions["fonts"] = await handleAsync(
			() => default_fonts(),
			ErrorCodes.FONT_LOAD_FAILED,
			"Failed to load default fonts for Satori"
		);
	}

	satoriOptions["loadAdditionalAsset"] = loadDynamicAsset({
		emoji: imageOptions.emoji as EmojiType,
	}) as SatoriOptions["loadAdditionalAsset"];

	logger.debug("Generating SVG with Satori");
	logger.info("VNode provided to satori:", JSON.stringify(vnodes, null, 2), "\n");
	logger.info("Options provided to satori:", imageOptions);

	return handleAsync(
		() => satori(vnodes, satoriOptions as SatoriOptions),
		ErrorCodes.SATORI_RENDER_FAILED,
		"Failed to render SVG with Satori"
	);
}

export async function createPng(
	element: string | Component,
	imageOptions: ImageOptions,
	componentOptions?: ComponentOptions
) {
	const svg = await handleAsync(
		() => createSvg(element, imageOptions, componentOptions),
		ErrorCodes.SATORI_RENDER_FAILED,
		"Failed to create SVG for PNG rendering"
	);

	logger.debug("SVG generated by satori for ReSVG: \n", svg, "\n");

	const resvg_instance = await handleAsync(
		() => useResvg(),
		ErrorCodes.RESVG_INIT_FAILED,
		"Failed to initialize ReSVG"
	);

	const resvg_options: ResvgRenderOptions = {
		fitTo: {
			mode: "width" as const,
			value: imageOptions.width || DEFAULT_WIDTH,
		},
	};

	logger.debug("Rendering PNG with ReSVG");
	logger.info("Options provided to ReSVG:", resvg_options, "\n");

	return handleAsync(
		async () => {
			const resvg = new resvg_instance(svg, resvg_options);
			const png_data = resvg.render();
			return png_data.asPng();
		},
		ErrorCodes.RESVG_RENDER_FAILED,
		"Failed to render PNG with ReSVG"
	);
}
